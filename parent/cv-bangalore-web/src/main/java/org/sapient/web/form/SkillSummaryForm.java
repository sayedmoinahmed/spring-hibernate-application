package org.sapient.web.form;

import java.io.Serializable;

import org.sapient.service.client.ProficiencyLevelType;

/**
 * @author komar
 *
 */
public class SkillSummaryForm implements Serializable {

	private Long skillInfoId;
	private Long skillTypeId;
	private String skillType;
	private String items;
	private ProficiencyLevelType proficienyLevel;
	/**
	 * @return the skillInfoId
	 */
	public Long getSkillInfoId() {
		return skillInfoId;
	}
	/**
	 * @param skillInfoId the skillInfoId to set
	 */
	public void setSkillInfoId(Long skillInfoId) {
		this.skillInfoId = skillInfoId;
	}
	/**
	 * @return the skillTypeId
	 */
	public Long getSkillTypeId() {
		return skillTypeId;
	}
	/**
	 * @param skillTypeId the skillTypeId to set
	 */
	public void setSkillTypeId(Long skillTypeId) {
		this.skillTypeId = skillTypeId;
	}
	/**
	 * @return the skillType
	 */
	public String getSkillType() {
		return skillType;
	}
	/**
	 * @param skillType the skillType to set
	 */
	public void setSkillType(String skillType) {
		this.skillType = skillType;
	}
	/**
	 * @return the items
	 */
	public String getItems() {
		return items;
	}
	/**
	 * @param items the items to set
	 */
	public void setItems(String items) {
		this.items = items;
	}
	/**
	 * @return the proficienyLevel
	 */
	public ProficiencyLevelType getProficienyLevel() {
		return proficienyLevel;
	}
	/**
	 * @param proficienyLevel the proficienyLevel to set
	 */
	public void setProficienyLevel(ProficiencyLevelType proficienyLevel) {
		this.proficienyLevel = proficienyLevel;
	}
	
}


