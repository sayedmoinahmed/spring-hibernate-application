package org.sapient.web.util;

import java.text.SimpleDateFormat;
import java.util.Date;

public class DateFormatUtil {

	private static final String EMPLOYMENT_DATE_FORMAT = "MMM-yyyy";

	public static String formatEmploymentDate(Date employmentDate) {
		return new SimpleDateFormat(EMPLOYMENT_DATE_FORMAT)
				.format(employmentDate);
	}
}
