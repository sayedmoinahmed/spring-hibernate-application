package org.sapient.web.form;


/**
 * @author brout
 * 
 */
public class CompletionTipsForm {

	private String educationDetails;
	private String employmentDetails;
	private String projectDetails;
	private String skillDetails;
	private String profileSummaryDetails;

	public CompletionTipsForm() {
		educationDetails ="Complete";
		employmentDetails ="Complete";
		projectDetails ="Complete";
		skillDetails ="Complete";
		profileSummaryDetails ="Complete";
	}

	/**
	 * @return the educationDetails
	 */
	public String getEducationDetails() {
		return educationDetails;
	}

	/**
	 * @param educationDetails
	 *            the educationDetails to set
	 */
	public void setEducationDetails(String educationDetails) {
		this.educationDetails = educationDetails;
	}

	/**
	 * @return the employmentDetails
	 */
	public String getEmploymentDetails() {
		return employmentDetails;
	}

	/**
	 * @param employmentDetails
	 *            the employmentDetails to set
	 */
	public void setEmploymentDetails(String employmentDetails) {
		this.employmentDetails = employmentDetails;
	}

	/**
	 * @return the projectDetails
	 */
	public String getProjectDetails() {
		return projectDetails;
	}

	/**
	 * @param projectDetails
	 *            the projectDetails to set
	 */
	public void setProjectDetails(String projectDetails) {
		this.projectDetails = projectDetails;
	}

	/**
	 * @return the skillDetails
	 */
	public String getSkillDetails() {
		return skillDetails;
	}

	/**
	 * @param skillDetails
	 *            the skillDetails to set
	 */
	public void setSkillDetails(String skillDetails) {
		this.skillDetails = skillDetails;
	}

	/**
	 * @return the profileSummaryDetails
	 */
	public String getProfileSummaryDetails() {
		return profileSummaryDetails;
	}

	/**
	 * @param profileSummaryDetails
	 *            the profileSummaryDetails to set
	 */
	public void setProfileSummaryDetails(String profileSummaryDetails) {
		this.profileSummaryDetails = profileSummaryDetails;
	}

}
